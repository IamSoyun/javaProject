package temp;

public class Printer {

	public static void println(int print) {
		System.out.println(print);
	}

	public static void println(boolean print) {
		System.out.println(print);
	}

	public static void println(double print) {
		System.out.println(print);
	}

	public static void println(String print) {
		System.out.println(print);
	}

}
