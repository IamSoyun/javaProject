package operators;

public class LogicalOperatorExample {
	public static void main(String[] args) {
		char charcode = 'A'; //65
		
		if(charcode >= 65 && charcode <= 90) 
			System.out.println("대문자임");
		
		if(charcode >= 97 && charcode <= 122)
			System.out.println("소문자임");
		
		
		
	}
	

}
